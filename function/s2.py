#  Addition
def add(a, b):
    return a + b

# Subtraction
def subtract(a, b):
    return a - b

#  Multiplication
def multiply(a, b):
    return a * b

# Division
def divide(a, b):
    return a / b

# Input values
x = 10
y = 5

# Call the functions and print the results
print(f"Addition: {x} + {y} = {add(x, y)}")
print(f"Subtraction: {x} - {y} = {subtract(x, y)}")
print(f"Multiplication: {x} * {y} = {multiply(x, y)}")
print(f"Division: {x} / {y} = {divide(x, y)}")
